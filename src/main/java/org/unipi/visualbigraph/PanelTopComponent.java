    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jProgressBar1.setStringPainted(true);
        jProgressBar1.setOpaque(true);
        jProgressBar1.setString("Loading...");
        SwingUtilities.invokeLater(new Runnable(){
            @Override
            public void run(){
                //jProgressBar1.setIndeterminate(true);
                wgu = WebGraphUtility.FactoryWebGraphUtility();
                //da fare il controllo se è presente il file
                if(wgu.importer(jTextField1.getText().trim())){
                    jComboBox2.setEnabled(true);
                    wgu.addGraph(jComboBox2);
                    jProgressBar1.setBackground(Color.GREEN);
                    jProgressBar1.setString("Loaded Correctly!");
                    jTabbedPane1.setEnabled(true);
                    jButton11.setEnabled(true);
                    jButton12.setEnabled(true);
                    jRadioButton1.setEnabled(true);
                }else{
                    jProgressBar1.setBackground(Color.RED);
                    jProgressBar1.setString("File Not Found!");
                    jTabbedPane1.setEnabled(false);
                    jButton11.setEnabled(false);
                    jButton12.setEnabled(false);
                }
            }
        });
        //jProgressBar1.setIndeterminate(false);
    }//GEN-LAST:event_jButton1ActionPerformed

