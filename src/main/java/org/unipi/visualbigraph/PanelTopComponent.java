    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        jProgressBar2.setStringPainted(true);
        jProgressBar2.setString("Searching...");
        SwingUtilities.invokeLater(new Runnable(){
            @Override
            public void run(){
                //jProgressBar2.setIndeterminate(true);
                Integer source = null , target = null;
                try{
                    source = Integer.parseInt(jTextField2.getText().trim());
                    target = Integer.parseInt(jTextField3.getText().trim());
                }catch(NumberFormatException e){
                    JOptionPane.showMessageDialog(null,"Node ID must be an Integer");
                    return;
                }
                if(wgu.shortestPath(source, target) == true){
                    jProgressBar2.setBackground(Color.GREEN);
                    jProgressBar2.setString("Found!");
                }else{
                    jProgressBar2.setBackground(Color.RED);
                    jProgressBar2.setString("Not Found! :(");
                }
            }
        });
        //jProgressBar1.setIndeterminate(false);
    }//GEN-LAST:event_jButton2ActionPerformed

